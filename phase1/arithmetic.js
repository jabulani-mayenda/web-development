// Basic arithmetic operations 
let a = 15; 
let b = 4; 
console.log("Addition:", a + b);        
console.log("Subtraction:", a - b);     
console.log("Multiplication:", a * b);  
console.log("Division:", a / b);        
console.log("Modulus (Remainder):", a % b); 
console.log("Exponentiation:", a ** b);  
let counter = 10; 
console.log("Original:", counter);      
console.log("Post-increment:", counter++);
console.log("After post-increment:", counter); 
console.log("Pre-increment:", ++counter);
console.log("Post-decrement:", counter--); 
console.log("After post-decrement:", counter); 