let score = parseInt(prompt("Enter your test score (0-100):")); 
if (score >= 90) { 
console.log("Grade: A - Excellent!"); 
} else if (score >= 80) { 
console.log("Grade: B - Very Good!"); 
} else if (score >= 70) { 
console.log("Grade: C - Good!"); 
} else if (score >= 60) { 
console.log("Grade: D - Passing!"); 
} else { 
console.log("Grade: F - Needs Improvement."); 
} 
